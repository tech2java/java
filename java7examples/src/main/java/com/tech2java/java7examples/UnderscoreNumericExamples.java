package com.tech2java.java7examples;

/**
 * int inum = 10_000_456;
 * long lnum = 10_000_456;
 * float fnum = 3.14_13_25F;
 * double dnum = 64_565.579_31_23;
 * long longBinary = 0b0_000_011_101_100_011;
 *
 */
public class UnderscoreNumericExamples {

    public static void main(String[] args) {

        int intNum=1_00_000;
        System.out.println(intNum);

        long longNum=5_00_000L;
        System.out.println(longNum);

        float floatNum=23_467.56_5f;
        System.out.println(floatNum);

        double doubleNum=4_05_678.876_5433d;
        System.out.println(doubleNum);

        int binaryNumber=0B01_000_11_11;
        System.out.println(binaryNumber);







    }

}
